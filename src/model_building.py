import os
import numpy as np
import pandas as pd  
import pickle
import logging 
from sklearn.ensemble import RandomForestRegressor
import yaml

log_dir='logs'
os.makedirs(log_dir,exist_ok=True)

logger=logging.getLogger('model_building')
logger.setLevel("DEBUG")

console_handler=logging.StreamHandler()
console_handler.setLevel('DEBUG')

log_file_path=os.path.join(log_dir,'model_building.log')
file_handler=logging.FileHandler(log_file_path)
file_handler.setLevel('DEBUG')

formatter=logging.Formatter('%(asctime)s-%(name)s-%(message)s')
console_handler.setFormatter(formatter)
file_handler.setFormatter(formatter)

logger.addHandler(console_handler)
logger.addHandler(file_handler)

def load_params(params_path:str)->dict:
    """load parameters from yaml file"""
    
    try:
        with open(params_path,'r') as file:
            params=yaml.safe_load(file)
        logger.debug('Parameters retrieved from %s',params_path)
        
        return params
    except FileNotFoundError as e:
        logger.error('File not found %s'.params_path)
        raise
    except yaml.YAMLError as e:
        logger.error('YAML error: %s',e)
        raise
    except Exception as e:
        logger.error('Unexpected error :%',e)
        
        raise
    
def load_data(file_path: str)->pd.DataFrame:
    """ Load data from a CSV file.
    
    :param file_path: Path to the CSV file
    :return: Loaded DataFrame"""
    try:
        df=pd.read_csv(file_path)
        logger.debug('Data loaded from %s with shape % s',file_path,df.shape)
        return df
    except pd.errors.ParserError as e:
        logger.error('Failed to parse the CSV file:%s',e)
        raise
    except Exception as e:
        logger.error('Unexpeced error %s',e)
        raise
    
def train_model(X_train:np.ndarray, y_train:np.ndarray, params:dict):
    """train the model"""
    
    try:
        if X_train.shape[0]!=y_train.shape[0]:
            raise ValueError('The number of samples in X_train and Y_train are different')
        logger.debug('Initializing random forest mdoel wiht parameters %s',params)
        
        clf=RandomForestRegressor(n_estimators=params['n_estimators'],random_state=params['random_state'])
        
        logger.debug("Model training started with %d samples",X_train.shape[0])
        clf.fit(X_train,y_train)
        logger.debug('Model training completed')
        
        return clf
    except ValueError as e:
        logger.error('ValueError during model traing %s',e)
        raise
    except Exception as e:
        logger.error('Error during model training %s',e)
        raise
    
def save_model(model,file_path):
    """save the trained model to file"""
    
    try:
        os.makedirs(os.path.dirname(file_path),exist_ok=True)
        
        with open(file_path,'wb') as file:
            pickle.dump(model,file)
        logger.debug('Model saved to %s',file_path)
    except FileNotFoundError as e:
        logger.error('File not found %s',e)
        raise
    except Exception as e:
        logger.error('Unexpected error %s',e)
        raise
    
def main():
    try:
        params=load_params('params.yaml')['model_building']
        train_data=load_data("data/interim/trained_processed.csv")
        X_train=train_data.iloc[:,:-1].values
        y_train=train_data.iloc[:,-1].values
        
        clf=train_model(X_train,y_train,params)
        
        model_save_path='models/model.pkl'
        save_model(clf,model_save_path)
        
    except Exception as e:
        logger.error('Failed to compute the model building process: %s',e)
        print(e)
        
if __name__=="__main__":
    main()
    

        

        